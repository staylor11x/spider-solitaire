package testtools

import (
	"github.com/staylor11x/spider-solitaire/internal/deck"
	"github.com/staylor11x/spider-solitaire/internal/game"
)

func MakeCard(s deck.Suit, r deck.Rank, faceUp bool) deck.Card {
	return deck.Card{Suit: s, Rank: r}
}

func MakeCardInPile(s deck.Suit, r deck.Rank, faceUp bool) game.CardInPile {
	return struct {
		Card   deck.Card
		FaceUp bool
	}{
		Card:   deck.Card{Suit: s, Rank: r},
		FaceUp: faceUp,
	}
}
